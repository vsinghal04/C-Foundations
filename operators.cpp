#include<iostream>
using namespace std;

int main() {
    int a = 10, b = 5;

    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;

    cout << "Is a > b? " << (a > b) << endl; // 1 for true,0 for false
    cout << "Is a == b? " << (a == b) << endl; //similar

    return 0;
}
